// bitwish
// and
console.log(5 & 3);
// ans = 1;
console.log(4 & 2);
// ans = 0;

// or
console.log(5 | 3);
// ans = 7;
console.log(4 | 2);
// ans = 6;

// NOT
console.log(~4);
// ans = -5;
console.log(~7);
// ans = -8;
